package org.jby.studyenglish.script;

public class Helloo {

    private String mEnglish;
    private String mMeaning;

    public Helloo(String english, String meaning){
        mEnglish = english;
        mMeaning = meaning;
    }

    public String getmEnglish() {
        return mEnglish;
    }

    public void setmEnglish(String mEnglish) {
        this.mEnglish = mEnglish;
    }

    public String getmMeaning() {
        return mMeaning;
    }

    public void setmMeaning(String mMeaning) {
        this.mMeaning = mMeaning;
    }
}
